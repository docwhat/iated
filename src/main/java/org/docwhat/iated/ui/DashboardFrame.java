    /* Initialize the Dashboard */
    private void init() {
        /* Get the application set up (macify). */
        application.addApplicationListener(this);
        application.addPreferencesMenuItem();
        application.setEnabledPreferencesMenu(true);

        /* Get the app state. */
        state = AppState.INSTANCE;

        /* Set the app state to start. */
        SwingUtilities.invokeLater(new Runnable() {

            public void run() {
                // Set up the state.
                state = AppState.INSTANCE;

                // Start the server.
                state.startServer();

                // Show the dashboard.
                setVisible(true);
            }
        });

        /* Initialize the UI parts. */
        initComponents();

        /* Update the display to set initial values. */
        updateDisplay();
    }

    private void updateDisplay() {
        activePortField.setText(String.valueOf(state.getActivePort()));
        if (state.isServerRunning()) {
            jToggleServer.setText("Running");
            jToggleServer.setSelected(true);
        } else {
            jToggleServer.setText("Stopped");
            jToggleServer.setSelected(false);
        }
        jToggleServer.setEnabled(true);
    }

